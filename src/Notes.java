public class Notes {


    //Object-oriented programming is a way to model real world objects as software objects, which contain both data and code.

    //Also called class based programming.

    //Classes become the blueprints for objects.

    //Real world objects have two major components: state and behavior.

    //State, using a computer as an example, would include things like total RAM, the OS it runs, hard drive size, etc.

    //These are characteristics that describe the item.

    //Objects may also have behavior.

    //Behavior are actions that can be performed by the object, or upon the object.

    //Behavior on a computer could be booting up, shutting down, etc.

    //Modelling real world objects as software objects is a fundamental part of OOP.

    //A software object stores state in fields, which can also be called variables, or attributes.

    //And objects expose their behavior with methods.

    //So a class is a template for creating objects.

    //The class describes data (fields) and the behavior (methods), that are relevant to the real world object we want to describe.
    //These are called class members.

    //A class member can be a field, or a method, or some other type of dependent element.

    //If a field is static, there is only one copy in memory, and this value is associated with the class, or template, itself.

    //If a field is not static, it's called an instance field, and each object may have a different value stored for this field.

    //A static method can't be dependent on any one object's state, so it can't reference any instance members.

    //So any method that operates on instance fields needs to be non-static.

    //Class or member fields can be thought of as variables, but it's more common to call them fields or attributes.

    //A class could be defined as a user-defined data type.


    //Classes can be organized into logical groupings, which are called packages.
    //You declare a package name in teh class using the package statement.
    //If you don't declare a package, the class implicitly belongs to the default package, which is all we've used so far.

    //A class is said to be a top-level class if it is defined in the source code file, and not enclosed in the code block
    //of another class, type, or method.

    //A top level class has only two valid access modifier options: public, or none.

    //public means any other class in any package can access this class.

    //none(omitted) is called package access, meaning the class is accessible only to classes in the same package.

}
